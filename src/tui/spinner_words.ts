// 运行中 spinner 的"思考/做事"动词,取自《道德经》《庄子》的语汇,随机轮换(对标 CC 的俏皮动名词)。
// 例:观其妙(道德经一章)、致虚守静(十六章)、涤除玄览(十章)、坐忘心斋·逍遥游(庄子)。
export const DAO_VERBS = [
  "悟", // 体悟
  "观", // 「以观其妙」
  "参", // 参详
  "思", // 沉思
  "究", // 探究
  "玩", // 玩味
  "酌", // 斟酌
  "衡", // 权衡
  "推", // 推演
  "索", // 探索
  "凝", // 凝神
  "省", // 内省
  "察", // 体察
  "度", // 揣度
  "游", // 「逍遥游」
  "化", // 「物化」
  "涤", // 「涤除玄览」
  "虚", // 「致虚极」
];

// 索引映射到词库(取模,负索引安全),便于按 tick/随机种子稳定展示。
export function daoVerb(n: number): string {
  const len = DAO_VERBS.length;
  return DAO_VERBS[((n % len) + len) % len]!;
}
